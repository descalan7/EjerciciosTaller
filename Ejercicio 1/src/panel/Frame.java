    private void cmdOpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdOpActionPerformed
        int op,n1,n2,n3,d1,d2,d3;
        Fraccionario f1,f2,f3=null;
        txtMixto.setText("");
        op= cmbOp.getSelectedIndex();
        n1=Integer.parseInt(txtNum1.getText());
        n2=Integer.parseInt(txtNum2.getText());
        d1=Integer.parseInt(txtDen1.getText());
        d2=Integer.parseInt(txtDen2.getText());
        try{
            f1= new Fraccionario(n1,d1);
            f2= new Fraccionario(n2,d2);
            switch(op){
                case 0:
                    f3=f1.suma(f2);
                    break;
                case 1:
                    f3=f1.resta(f2);
                    break;
                case 2:
                    f3=f1.multiplicacion(f2);
                    break;
                   
                case 3:
                    f3=f1.division(f2);
                    
            }
            txtNum3.setText(""+f3.getNumerador());
            txtDen3.setText(""+f3.getDenominador());
            
            
        }catch(DenominadorCeroException e){
            Helper.mensaje(null, "No puede digitar el n√∫mero 0 en el Denominador", "Error", 2);
        }
        
        
    }//GEN-LAST:event_cmdOpActionPerformed

